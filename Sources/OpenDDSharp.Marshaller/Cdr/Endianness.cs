/// Represents the byte order of the CDR stream.
/// </summary>
public enum Endianness
{
    /// <summary>
    /// Big endian byte order.
    /// </summary>
    BigEndian,
    /// <summary>
    /// Little endian byte order.
    /// </summary>
    LittleEndian,
}